package com.qfedu.vo;

public class JsonBean {
    private int code;
    private String info;

    public JsonBean() {
    }

    public JsonBean(int code, String info) {
        this.code = code;
        this.info = info;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }
}
